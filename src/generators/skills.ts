function skillContent(
  name: string,
  analysis: RepositoryAnalysis,
  body: string[],
): string {
  return [
    `# ${name} — ${analysis.repoName}`,
    "",
    ...body,
    "",
    "---",
    `*Generated by claude-onboard for ${analysis.repoName}*`,
    "",
  ].join("\n");
}

function makeFrameworkSkill(
  framework: string,
  filename: string,
  depCheck: string,
  tips: string[],
): SkillTemplate {
  return {
    filename,
    condition: (a) => a.frameworks.includes(framework),
    render: (a) =>
      skillContent(framework, a, [
        "## When to Use",
        `When working with ${framework}-specific code.`,
        "",
        "## Repo-Specific Context",
        ...tips,
        "",
        "## Common Gotchas",
        `- Check ${framework} documentation for version-specific behavior`,
        `- Review existing ${framework} patterns in this repo before adding new ones`,
      ]),
  };
}

