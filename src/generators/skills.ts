export class SkillBuilder {
  constructor(
    private readonly repoPath: string,
    private readonly analysis: RepositoryAnalysis,
  ) {}

  async buildAll(): Promise<GeneratedFile[]> {
    const allTemplates = [...BASE_SKILLS, ...FRAMEWORK_SKILLS];
    const files: GeneratedFile[] = [];

    for (const template of allTemplates) {
      if (template.condition(this.analysis)) {
        const content = template.render(this.analysis);
        files.push({
          path: `.claude/skills/${template.filename}`,
          content,
          action: "created",
        });
      }
    }

    return files;
  }
}
